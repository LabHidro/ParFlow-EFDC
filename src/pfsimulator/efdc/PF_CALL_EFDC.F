	SUBROUTINE pf_call_efdc(PF_EFDC_NPROC,INFO,EFDCCOMM,EFDCINTERCOMM)
	
	USE GLOBAL
	USE MPI
   
	INTEGER EFDCCOMM,EFDCINTERCOMM
	INTEGER :: PF_EFDC_NPROC, INFO, IERR, send01, REQUEST, ind, RECEIVE(1), my_id
	INTEGER from, tag
	!integer, dimension(MPI_STATUS_SIZE) :: status		
		
	CALL MPI_COMM_RANK(EFDCCOMM, my_id, IERR)	
	
	from = my_id
	tag = 1
			
	CALL MPI_COMM_SPAWN('EFDC', MPI_ARGV_NULL, PF_EFDC_NPROC, INFO, 0, EFDCCOMM, EFDCINTERCOMM, MPI_ERRCODES_IGNORE, IERR)
			
	CALL MPI_Irecv(RECEIVE(1), 1, MPI_INTEGER, from, tag,EFDCINTERCOMM, REQUEST,IERR)
	
	CALL MPI_WAIT(REQUEST,MPI_STATUS_IGNORE,IERR)
	
	END SUBROUTINE pf_call_efdc